package com.app.other.permissions

/**
 * В Android разрешения (permissions) используются для контроля доступа приложений к определенным ресурсам и функциям устройства, таким как доступ к интернету, камере, геолокации и т.д. Разрешения делятся на две основные категории:
 *
 * 1. Обычные разрешения (Normal Permissions)
 * Эти разрешения не требуют подтверждения пользователя и не представляют значительной угрозы безопасности или конфиденциальности. Примеры:
 *
 * INTERNET
 * ACCESS_NETWORK_STATE
 * VIBRATE
 * Для использования этих разрешений достаточно просто указать их в манифесте приложения:
 *
 * <uses-permission android:name="android.permission.INTERNET" />
 * 2. Опасные разрешения (Dangerous Permissions)
 * Эти разрешения требуют явного согласия пользователя, так как они могут затрагивать его конфиденциальность и безопасность. Примеры:
 *
 * READ_CONTACTS
 * ACCESS_FINE_LOCATION
 * CAMERA
 * Для использования этих разрешений нужно сначала указать их в манифесте, а затем запрашивать разрешение во время выполнения приложения.
 *
 * Запрос разрешений во время выполнения
 * С Android 6.0 (API 23) необходимо запрашивать опасные разрешения в коде приложения. Вот как это делается:
 *
 * Проверка разрешения: Перед выполнением действия, требующего разрешения, проверьте, есть ли у приложения это разрешение.
 *
 * if (ContextCompat.checkSelfPermission(this, Manifest.permission.CAMERA)
 *         != PackageManager.PERMISSION_GRANTED) {
 *     // Разрешение не получено
 * }
 * Запрос разрешения: Если разрешение не получено, запросите его у пользователя.
 *
 * ActivityCompat.requestPermissions(this,
 *         new String[]{Manifest.permission.CAMERA},
 *         REQUEST_CAMERA_PERMISSION);
 * Обработка результата запроса: Переопределите метод onRequestPermissionsResult для обработки ответа пользователя.
 *
 * @Override
 * public void onRequestPermissionsResult(int requestCode,
 *                                        String[] permissions,
 *                                        int[] grantResults) {
 *     switch (requestCode) {
 *         case REQUEST_CAMERA_PERMISSION: {
 *             if (grantResults.length > 0 && grantResults[0] == PackageManager.PERMISSION_GRANTED) {
 *                 // Разрешение предоставлено
 *             } else {
 *                 // Разрешение отклонено
 *             }
 *             return;
 *         }
 *     }
 * }
 *
 * 4. Специальные разрешения
 * Некоторые разрешения требуют дополнительных действий, например:
 *
 * Разрешения для работы в фоновом режиме: начиная с Android 8.0, для некоторых действий в фоновом режиме (например, получение местоположения) необходимо использовать специальные разрешения и управлять ими.
 * Разрешения для доступа к файлам: с Android 10 введены ограничения на доступ к файлам в хранилище, которые требуют использования Scoped Storage.
 * */